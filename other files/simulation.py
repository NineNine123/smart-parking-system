while True:

    frame = background.copy()

    # load live parking status
    if os.path.exists("parking_status.json"):

        with open("parking_status.json", "r") as f:
            parking_status = json.load(f)

    else:

        parking_status = {}

    occupied_count = 0
    available_count = 0

    # draw slots
    for slot_id, points in map_slots.items():

        pts = np.array(points, np.int32)

        status = parking_status.get(
            slot_id,
            "AVAILABLE"
        )

        # occupied
        if status == "OCCUPIED":

            color = (0, 0, 255)
            occupied_count += 1

        # available
        else:

            color = (0, 255, 0)
            available_count += 1

        # transparent overlay
        overlay = frame.copy()

        cv2.fillPoly(
            overlay,
            [pts],
            color
        )

        frame = cv2.addWeighted(
            overlay,
            0.35,
            frame,
            0.65,
            0
        )

        # border
        cv2.polylines(
            frame,
            [pts],
            isClosed=True,
            color=color,
            thickness=2
        )

        # slot label
        cv2.putText(
            frame,
            slot_id,
            tuple(points[0]),
            cv2.FONT_HERSHEY_SIMPLEX,
            0.5,
            color,
            2
        )

    # show counts
    cv2.putText(
        frame,
        f"Available: {available_count}",
        (40, 50),
        cv2.FONT_HERSHEY_SIMPLEX,
        1,
        (0, 255, 0),
        3
    )

    cv2.putText(
        frame,
        f"Occupied: {occupied_count}",
        (40, 100),
        cv2.FONT_HERSHEY_SIMPLEX,
        1,
        (0, 0, 255),
        3
    )

    # show simulation
    cv2.imshow(
        "Parking Simulation",
        frame
    )

    # quit
    if cv2.waitKey(500) & 0xFF == ord("q"):
        break

cv2.destroyAllWindows()